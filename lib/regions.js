// 시도 ID로 검색
export function getRegion(id) {
  return REGIONS.find(r => r.id === id)
}

// 시군구명으로 시도 찾기
export function findRegionByDistrict(districtName) {
  return REGIONS.find(r =>
    r.districts.some(d => districtName.includes(d) || d.includes(districtName))
  )
}
